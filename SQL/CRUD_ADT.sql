CREATE DATABASE IF NOT EXISTS CRUD_ADT;
USE CRUD_ADT;

CREATE TABLE PROFILE_(
USER_CODE INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
EMAIL VARCHAR (40) UNIQUE,
USER_NAME VARCHAR (30) UNIQUE,
PSWD VARCHAR (30),
TELEPHONE INT,
NAME_ VARCHAR (30),
SURNAME VARCHAR (30)
);

CREATE TABLE USER_(
USER_CODE INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
EMAIL VARCHAR (40) UNIQUE,
USER_NAME VARCHAR (30) UNIQUE,
PSWD VARCHAR (30),
TELEPHONE INT,
NAME_ VARCHAR (30),
SURNAME VARCHAR (30),
GENDER VARCHAR (10),
CARD_NO VARCHAR (50),
FOREIGN KEY (USER_CODE) REFERENCES PROFILE_(USER_CODE) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE ADMIN_(
USER_CODE INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
EMAIL VARCHAR (40) UNIQUE,
USER_NAME VARCHAR (30) UNIQUE,
PSWD VARCHAR (30),
TELEPHONE INT,
NAME_ VARCHAR (30),
SURNAME VARCHAR (30),
CURRENT_ACCOUNT VARCHAR (50),
FOREIGN KEY (USER_CODE) REFERENCES PROFILE_(USER_CODE) ON UPDATE CASCADE ON DELETE CASCADE
);


